#Think of your favorite mode of transportation, such as a motorcycle or a car, and make a list that stores several examples.
#Use your list to print a series of statements about these items, such as “I would like to own a Honda motorcycle.”

#list of car brands
cars = ["Dodge", "Porsche", "Lamborghini"]

#for loop
for car in cars:
    #f "formatted sting" transforms the variable "cars" to its values in the lists when printed
    print(f"I would like to own a {car}.")